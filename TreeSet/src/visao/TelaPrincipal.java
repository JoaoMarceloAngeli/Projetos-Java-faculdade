    private void jButton_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarActionPerformed
        // TODO add your handling code here:
         try {
        BuscarTela tela_buscar = new BuscarTela(this,true);
        tela_buscar.setLocationRelativeTo(null);
        tela_buscar.setVisible(true);
        Professor resultadoBusca = new Professor();
        int infBusca = -1;
        infBusca = tela_buscar.exportarDadosBusca(infBusca);
        if(infBusca != -1){
        Iterator<Professor> lista = professor_Matricula.iterator();
        while(lista.hasNext()){
            Professor novoProf = new Professor();
            novoProf = lista.next();
            if(novoProf.getMatricula() == infBusca)
                resultadoBusca = novoProf;
        }
        if(resultadoBusca!=null){
        DefaultTableModel model = (DefaultTableModel) jTable_Saida.getModel();
        model.setNumRows(0);
        String[] saida = new String[5];
            saida[0] = resultadoBusca.getMatricula() + "";
            saida[1] = resultadoBusca.getNome() + "";
             saida[2] = resultadoBusca.getEscola() + "";
            saida[3] = resultadoBusca.getTitulacao() + "";
            saida[4] = resultadoBusca.getContrato() + "";
            model.addRow(saida);
        }
        }
        } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage() + "excluir\n");
        }
    }//GEN-LAST:event_jButton_BuscarActionPerformed

    private void jButton_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ExcluirActionPerformed
        // TODO add your handling code here:
        try {
        DeletarTela tela_deletar = new DeletarTela(this,true);
        tela_deletar.setLocationRelativeTo(null);
        tela_deletar.setVisible(true);
        int infDelete = -1;
        infDelete = tela_deletar.exportarDadosDeletar(infDelete);
        if(infDelete != -1){
        Iterator<Professor> lista = professor_Matricula.iterator();
        while(lista.hasNext()){
            Professor novoProf = new Professor();
            novoProf =  lista.next();
            if(novoProf.getMatricula() == infDelete){
                professor_Matricula.remove(novoProf);
                JOptionPane.showMessageDialog(this, "DADO EXCLUIDO COM SUCESSO LIST PARA VER");
                return;
            }                 
        }
        }
        } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage() + "excluir\n");
        }
    }//GEN-LAST:event_jButton_ExcluirActionPerformed

    private void jButton_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AlterarActionPerformed
        // TODO add your handling code here:
        try {
        Professor novo = new Professor();
        AlterarTela tela_alterar = new AlterarTela(this,true);
        tela_alterar.listar(professor_Matricula.iterator());
        tela_alterar.setLocationRelativeTo(null);
        tela_alterar.setVisible(true);
        novo = tela_alterar.exportarDadosAlterar(novo);
        if(novo != null){
        Iterator<Professor> lista = professor_Matricula.iterator();
        while(lista.hasNext()){
            Professor novoProf = new Professor();
            novoProf =  lista.next();
            if(novoProf.getMatricula() == novo.getMatricula()){
                professor_Matricula.remove(novoProf);
                professor_Matricula.add(novo);
                JOptionPane.showMessageDialog(this, "DADO alterado COM SUCESSO LIST PARA VER");
                return;
            }
        }
        }
        } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage() + "alterar\n");
        }
    }//GEN-LAST:event_jButton_AlterarActionPerformed
