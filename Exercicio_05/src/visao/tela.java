    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        calculo calculo = new calculo();
        String valor = Numero.getText(); 
        int decimalNumber = Integer.parseInt(valor); 
        if (decimalNumber < 0) {
            Resposta.setText("Por favor, insira um número não negativo.");
            return;
        }
        String binaryString = calculo.decimalParaBinario(decimalNumber); 
        Resposta.setText("Resultado: " + binaryString); 
    } catch (NumberFormatException ex) {
        Resposta.setText("Por favor, insira um número válido.");
    } catch (StackOverflowError e) {
        Resposta.setText("Erro de cálculo: o número é muito grande. Tente um valor menor.");
    }   
    }//GEN-LAST:event_jButton1ActionPerformed

